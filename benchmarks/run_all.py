"""Automated controller benchmark.

Runs every controller across every standard scenario, computes performance metrics,
estimates each controller's region of attraction, and measures Monte-Carlo robustness to
parameter uncertainty. Outputs a CSV, comparison plots, ROA maps, and a Markdown report
under ``benchmarks/results/``.

Usage:
    python benchmarks/run_all.py              # full run
    python benchmarks/run_all.py --quick      # small grids/samples for a fast smoke run
"""

from __future__ import annotations

import sys as _sys
from pathlib import Path as _Path

# Allow running directly from a source checkout (before `pip install -e .`).
_src = _Path(__file__).resolve().parents[1] / "src"
if _src.is_dir() and str(_src) not in _sys.path:
    _sys.path.insert(0, str(_src))

import argparse
import csv
from pathlib import Path

from segway.analysis import compute_roa, monte_carlo
from segway.config import RobotParams, SimConfig
from segway.controllers import REGULATORS, build_controller
from segway.sim import STANDARD_SCENARIOS, Scenario, simulate
from segway.viz import plot_comparison, plot_roa

RESULTS = Path("benchmarks/results")

# A deliberately hard robustness regime (near the fall boundary, wide parameter spread) so
# the success rate actually discriminates between controllers.
ROBUSTNESS_SCENARIO = Scenario.balance(1.0)
ROBUSTNESS_RANGES = {"m_pend": 0.6, "l": 0.4, "I_pend": 0.6, "b_x": 0.8, "b_theta": 0.8}
METRIC_COLS = [
    "scenario", "controller", "fell", "settling_time_angle", "peak_angle_deg",
    "control_effort", "total_abs_torque", "max_pos_drift_m",
]


def _fmt(v) -> str:
    if isinstance(v, bool):
        return "yes" if v else "no"
    if isinstance(v, float):
        return "∞" if v == float("inf") else f"{v:.3f}"
    return str(v)


def run_metrics(controllers, params, sim):
    rows = []
    for scen_name, scen in STANDARD_SCENARIOS.items():
        trajs = {}
        for cname in controllers:
            traj = simulate(params, build_controller(cname, params), scen, sim)
            trajs[cname] = traj
            m = traj.metrics()
            rows.append({"scenario": scen_name, "controller": cname,
                         **{k: m[k] for k in METRIC_COLS[2:]}})
        plot_comparison(trajs, RESULTS / f"compare_{scen_name}.png",
                        title=f"Controller comparison - {scen_name}")
    return rows


def run_roa(controllers, params, n):
    areas = {}
    for cname in controllers:
        res = compute_roa(params, build_controller(cname, params), n_theta=n, n_thetadot=n)
        areas[cname] = res.area_fraction
        plot_roa(res, RESULTS / f"roa_{cname}.png")
    return areas


def run_robustness(controllers, n):
    return {
        c: monte_carlo(c, scenario=ROBUSTNESS_SCENARIO, ranges=ROBUSTNESS_RANGES, n=n).success_rate
        for c in controllers
    }


def _md_table(headers, rows) -> str:
    out = ["| " + " | ".join(headers) + " |", "|" + "|".join(["---"] * len(headers)) + "|"]
    for r in rows:
        out.append("| " + " | ".join(_fmt(x) for x in r) + " |")
    return "\n".join(out)


def write_report(metric_rows, roa_areas, robustness, path: Path) -> None:
    lines = ["# Benchmark Report", "", "_Auto-generated by `benchmarks/run_all.py`._", ""]

    for scen_name in STANDARD_SCENARIOS:
        lines += [f"## Scenario: `{scen_name}`", ""]
        scen_rows = [r for r in metric_rows if r["scenario"] == scen_name]
        table_rows = [[r["controller"], r["fell"], r["settling_time_angle"],
                       r["peak_angle_deg"], r["control_effort"], r["max_pos_drift_m"]]
                      for r in scen_rows]
        lines += [_md_table(["controller", "fell", "settle θ (s)", "peak (deg)",
                             "effort", "max drift (m)"], table_rows), "",
                  f"![comparison](compare_{scen_name}.png)", ""]

    lines += ["## Region of attraction (recoverable area)", ""]
    lines += [_md_table(["controller", "ROA area"],
                        [[c, f"{a:.0%}"] for c, a in roa_areas.items()]), ""]
    for c in roa_areas:
        lines += [f"![roa {c}](roa_{c}.png)"]
    lines += [""]

    lines += ["## Robustness (success rate under ±parameter uncertainty)", "",
              f"_Designed on the nominal plant, tested on randomized plants_ "
              f"(`{ROBUSTNESS_SCENARIO.name}`; mass ±60%, length ±40%, inertia ±60%, "
              f"damping ±80%)._", ""]
    lines += [_md_table(["controller", "success rate"],
                        [[c, f"{s:.0%}"] for c, s in robustness.items()]), ""]

    path.write_text("\n".join(lines), encoding="utf-8")


def main(argv=None) -> int:
    ap = argparse.ArgumentParser(description="Run the controller benchmark")
    ap.add_argument("--quick", action="store_true", help="small grids/samples for speed")
    ap.add_argument("--roa-n", type=int, default=None)
    ap.add_argument("--mc-n", type=int, default=None)
    args = ap.parse_args(argv)

    roa_n = args.roa_n or (9 if args.quick else 21)
    mc_n = args.mc_n or (20 if args.quick else 150)

    RESULTS.mkdir(parents=True, exist_ok=True)
    params = RobotParams()
    sim = SimConfig(duration=8.0)
    controllers = list(REGULATORS)  # regulation benchmark; swing-up is showcased separately

    print(f"Controllers: {controllers}")
    print("[1/3] metrics + comparison plots ...")
    metric_rows = run_metrics(controllers, params, sim)
    with (RESULTS / "metrics.csv").open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=METRIC_COLS)
        w.writeheader()
        w.writerows(metric_rows)

    print(f"[2/3] region of attraction ({roa_n}x{roa_n}) ...")
    roa_areas = run_roa(controllers, params, roa_n)

    print(f"[3/3] Monte-Carlo robustness (n={mc_n}) ...")
    robustness = run_robustness(controllers, mc_n)

    write_report(metric_rows, roa_areas, robustness, RESULTS / "report.md")
    print(f"\nDone. See {RESULTS / 'report.md'}")
    print("ROA areas:", {c: f"{a:.0%}" for c, a in roa_areas.items()})
    print("Robustness:", {c: f"{s:.0%}" for c, s in robustness.items()})
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
